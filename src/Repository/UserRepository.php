class UserRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, User::class);
    }

    public function findNonAdminUnverifiedCustomers()
    {
        return $this->createQueryBuilder('u')
            ->innerjoin('u.customer', 'c') // Fazendo o join com a entidade Customer (relacionamento deve existir)
            ->where('u.isAdmin = :isAdmin') // Apenas usuários que não são administradores
            ->andWhere('c.IsChecked = :IsChecked') // Apenas clientes não verificados
            ->setParameter('isAdmin', false)
            ->setParameter('IsChecked', false)
            ->getQuery()
            ->getResult();
    }

